function getUserLocation() {
  return new Promise(resolve => {
    if (!navigator.geolocation) {
      resolve({ lat: 42.1, lng: -71.8 });
      return;
    }

    navigator.geolocation.getCurrentPosition(
      pos => resolve({
        lat: pos.coords.latitude,
        lng: pos.coords.longitude
      }),
      () => resolve({ lat: 42.1, lng: -71.8 }),
      { timeout: 5000 }
    );
  });
}

