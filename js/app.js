function rateSpeed(e, id, rating) {

  e.stopPropagation();

  rating = Number(rating);

  if (!canRateSpeed(id)) {
    alert(
      "⏳ You already rated speed here. Try again later."
    );
    return;
  }

  recordSpeedRating(id);

  trackEvent("rate_speed", {
    location_id: id,
    rating: rating
  });

  db.collection("votes")
    .doc(id)
    .set({

      speedTotal:
        firebase.firestore.FieldValue.increment(
          rating
        ),

      speedVotes:
        firebase.firestore.FieldValue.increment(
          1
        )

    }, { merge:true });

}

