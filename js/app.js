let isSubmittingCoffeeRun = false;

function submitCoffeeRun(speedRating) {

  if (!activeCoffeeLocationId) return;
  if (isSubmittingCoffeeRun) return;

  if (selectedAccuracy === null) {

    alert(
      "Please select whether your order was correct."
    );

    return;
  }

  const locId = activeCoffeeLocationId;

  // Pre-flight check
  if (!canVote(locId) || !canRateSpeed(locId)) {

    alert(
      "⏳ You already rated this location recently."
    );

    closeCoffeeRunSheet();
    return;
  }

  isSubmittingCoffeeRun = true;

  // Disable buttons visually to prevent double clicks
  const buttons =
    document.querySelectorAll(
      "#coffeeRunSheet button"
    );

  buttons.forEach(
    btn => btn.disabled = true
  );

  const isUp = selectedAccuracy;

  const voteRef =
    db.collection("votes")
      .doc(locId);

  voteRef.set({

    upvotes:
      firebase.firestore.FieldValue.increment(
        isUp ? 1 : 0
      ),

    downvotes:
      firebase.firestore.FieldValue.increment(
        isUp ? 0 : 1
      ),

    speedTotal:
      firebase.firestore.FieldValue.increment(
        speedRating
      ),

    speedVotes:
      firebase.firestore.FieldValue.increment(
        1
      )

  }, { merge:true })

  .then(() => {

    // 🐧 Penguin Patrol credit

	if (auth.currentUser) {

	  db.collection("scouts")
		.doc(auth.currentUser.uid)
		.update({

		  voteCount:
			firebase.firestore.FieldValue.increment(1)

		})
		.then(() => {

		  loadTopScouts();

		  loadPatrolBar(
			auth.currentUser
		  );

		})
		.catch(err =>
		  console.error(
			"Scout vote update failed:",
			err
		  )
		);

	}

    isSubmittingCoffeeRun = false;

    buttons.forEach(
      btn => btn.disabled = false
    );

    // Save local cooldowns
    recordVote(locId);
    recordSpeedRating(locId);

    // Analytics
    trackEvent("vote", {
      location_id: locId,
      type: isUp ? "upvote" : "downvote"
    });

    trackEvent("rate_speed", {
      location_id: locId,
      rating: speedRating
    });

    closeCoffeeRunSheet();

    const toast =
      document.getElementById(
        "speedSuccessToast"
      );

    if (toast) {

      toast.textContent =
        "Thanks for rating your coffee run! ☕";

      toast.classList.remove("hidden");

      setTimeout(() => {
        toast.classList.add("hidden");
      }, 3000);

    }

  })

  .catch(err => {

    isSubmittingCoffeeRun = false;

    buttons.forEach(
      btn => btn.disabled = false
    );

    console.error(
      "❌ Coffee run submission failed:",
      err
    );

    alert(
      "❌ Rating submission failed. Please check your connection and try again."
    );

  });

}

