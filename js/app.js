function getTopPicks(locations) {

  if (!locations.length) {
    return {
      overall: null,
      fastest: null,
      best: null
    };
  }

  // ЁЯза Best Overall (Highest calculated algorithmic score)
  const overall = locations[0];

  // тЪб Fastest (Lowest average speed rating score = fastest service)
  const fastest =
    [...locations]
      .filter(l => l.speed > 0)
      .sort((a, b) => a.speed - b.speed)[0];

  // тнР Best Quality (Highest accurate order percentage)
  const best =
    [...locations]
      .sort((a, b) => b.percent - a.percent)[0];

  return {
    overall,
    fastest,
    best
  };
}

