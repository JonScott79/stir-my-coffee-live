async function getAddress(lat, lng) {
  const key = `addr_${lat}_${lng}`;

  const cached = localStorage.getItem(key);
  if (cached) return cached;

  try {
    const res = await fetch(
      `https://nominatim.openstreetmap.org/reverse?lat=${lat}&lon=${lng}&format=json`
    );

    const data = await res.json();
    if (!data || !data.address) return null;

    const addr = data.address;

    // 🔥 BETTER STREET LOGIC
    const streetAddress =
      [addr.house_number, addr.road].filter(Boolean).join(" ") ||
      addr.neighbourhood ||
      addr.suburb ||
      addr.road || // fallback if partial
      "";

    const city =
      addr.city ||
      addr.town ||
      addr.village ||
      addr.hamlet ||
      "";

    const fullAddress = [streetAddress, city]
      .filter(Boolean)
      .join(", ");

    if (fullAddress) {
      localStorage.setItem(key, fullAddress);
    }

    return fullAddress || null;

  } catch (err) {
    console.error("Address lookup failed:", err);
    return null;
  }
}

async function loadAddressesForVisible(locations) {
  let updated = false;

  for (const loc of locations) {
    // Skip if it already has an address
    if (loc.street !== null) continue;

    // Skip if it's already actively fetching
    if (loc.loadingAddress) continue;

    // Cooldown check to prevent hammering the API
    if (loc.lastAddressAttempt && Date.now() - loc.lastAddressAttempt < 5000) {
      continue;
    }

    loc.loadingAddress = true;
    loc.lastAddressAttempt = Date.now();

    try {
      // ⏳ FORCE A 1-SECOND PAUSE BEFORE EACH REQUEST TO RESPECT RATE LIMITS
      await new Promise(resolve => setTimeout(resolve, 1000));

      const addr = await getAddress(loc.lat, loc.lng);
      
      if (addr) {
        loc.street = addr;
        updated = true;
        
        // 🔥 RE-RENDER IMMEDIATELY as each address lands so the user sees progress
        updateDistancesAndSort();
      }
    } catch (err) {
      console.error("Address lookup failed:", err);
    } finally {
      loc.loadingAddress = false;
    }
  }
}

async function loadLocations() {
  loaderVisible = false;
  scheduleLoader();

  try {
    // ========================
    // 🔥 STEP 0: LOAD CACHED VOTES
    // ========================
    let voteMap = {};
    const cachedVotes = localStorage.getItem("cachedVotes");

    if (cachedVotes) {
      const parsedVotes = JSON.parse(cachedVotes);
      const age = Date.now() - parsedVotes.timestamp;
      const maxAge = 1000 * 60 * 10;

      if (age < maxAge) {
        voteMap = parsedVotes.data;
      }
    }

    // ========================
    // 🔥 STEP 1: LOAD LOCATIONS FROM FIREBASE (PRIMARY)
    // ========================
    const snapshot = await db.collection("locations").get();

    const firebaseData = snapshot.docs.map(doc => {
      const d = doc.data();
      const id = doc.id;

      const v = voteMap[id] || {};

      const up = v.upvotes || 0;
      const down = v.downvotes || 0;
      const total = up + down;

      const speedTotal = v.speedTotal || 0;
      const speedVotes = v.speedVotes || 0;

      return {
        id,
        name: d.name,
        lat: Number(d.lat),
        lng: Number(d.lng),
        city: d.city || "",
        state: d.state || "",
        street: null,
        percent: total ? Math.round((up / total) * 100) : 0,
        speed: speedVotes ? (speedTotal / speedVotes) : 0,
        votes: total,
        distance: 0
      };
    });

    allLocations = firebaseData;

    locationsReady = true;
    dataFullyReady = true;

    tryRender();

    // ========================
    // 💾 CACHE (SLIM VERSION)
    // ========================
    try {
      const slim = firebaseData.map(loc => ({
        id: loc.id,
        name: loc.name,
        lat: loc.lat,
        lng: loc.lng,
        city: loc.city,
        state: loc.state
      }));

      localStorage.setItem("cachedLocations", JSON.stringify({
        data: slim,
        timestamp: Date.now()
      }));

    } catch (e) {
      console.warn("⚠️ Cache skipped");
    }

    updateDistancesAndSort();

    // ========================
    // 🔥 STEP 2: REFRESH VOTES (ASYNC)
    // ========================
    loadVotes().then(votes => {
      allLocations.forEach(loc => {
        const v = votes[loc.id];
        if (!v) return;

        const up = v.upvotes || 0;
        const down = v.downvotes || 0;
        const total = up + down;

        const speedTotal = v.speedTotal || 0;
        const speedVotes = v.speedVotes || 0;

        loc.percent = total ? Math.round((up / total) * 100) : 0;
        loc.votes = total;
        loc.speed = speedVotes ? (speedTotal / speedVotes) : 0;
      });

      updateDistancesAndSort();
    });

    // ========================
    // 🛑 FAILSAFE
    // ========================
    setTimeout(() => {
      hideLoader();
    }, 1500);

  } catch (err) {
    console.error("LOAD FAILED:", err);
    hideLoader();
  }
}

