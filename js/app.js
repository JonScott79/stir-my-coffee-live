function canVote(id) {
  const history = getVoteHistory();
  const lastVote = history[id];

  if (!lastVote) return true;

  const now = Date.now();
  const hoursPassed = (now - lastVote) / (1000 * 60 * 60);

  return hoursPassed >= VOTE_LIMIT_HOURS;
}

function recordVote(id) {
  const history = getVoteHistory();
  history[id] = Date.now();
  saveVoteHistory(history);
}

